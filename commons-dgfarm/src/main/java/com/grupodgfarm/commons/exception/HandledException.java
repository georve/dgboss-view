package com.grupodgfarm.commons.exception;

public class HandledException extends DgFarmException {

	/**
	 * Serial generado aleatoriamente.
	 */
	private static final long serialVersionUID = 6311263159049919350L;

	public HandledException() {
		super();
	}

	public HandledException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public HandledException(String arg0) {
		super(arg0);
	}

	public HandledException(Throwable arg0) {
		super(arg0);
	}
	
	/**
	 * constructor de la clase se maneja todas las propiedades referentes a las transacciones
	 * y detalles de la aplicacion.
	 * @param aplicacion
	 * @param origen
	 * @param codigo
	 * @param mensaje
	 * @param transaccion
	 * @param plataforma
	 * @param paramEntrada
	 * @param e
	 */
	public HandledException(String aplicacion,String origen,
			                 int codigo,String mensaje,String transaccion,String plataforma,String paramEntrada, Throwable e){
		super(aplicacion,origen,codigo,mensaje,transaccion,plataforma,paramEntrada,e);
	}

}
